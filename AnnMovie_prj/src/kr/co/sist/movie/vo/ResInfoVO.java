package kr.co.sist.movie.vo;

public class ResInfoVO {
	private String mName, wDate, wTime;
	private Integer resNum, seatNum, resCnt;
	public String getmName() {
		return mName;
	}
	public void setmName(String mName) {
		this.mName = mName;
	}
	public String getwDate() {
		return wDate;
	}
	public void setwDate(String wDate) {
		this.wDate = wDate;
	}
	public String getwTime() {
		return wTime;
	}
	public void setwTime(String wTime) {
		this.wTime = wTime;
	}
	public Integer getResNum() {
		return resNum;
	}
	public void setResNum(Integer resNum) {
		this.resNum = resNum;
	}
	public Integer getSeatNum() {
		return seatNum;
	}
	public void setSeatNum(Integer seatNum) {
		this.seatNum = seatNum;
	}
	public Integer getResCnt() {
		return resCnt;
	}
	public void setResCnt(Integer resCnt) {
		this.resCnt = resCnt;
	}
	
	
}
